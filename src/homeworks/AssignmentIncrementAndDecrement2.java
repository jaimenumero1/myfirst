package homeworks;

public class AssignmentIncrementAndDecrement2 {

    public static void main(String[] args) {

        int x = 1 ;

        System.out.println(x);//1 Correct

        System.out.println(++x); // 2 Correct

        System.out.println(x++); // 2 , then x becomes 3 Correct

        System.out.println(--x); // 2 correct

        System.out.println(x--);// 2 then becomes 1 Correct

        System.out.println(x);//1 Current Value     Correct

    }
}
